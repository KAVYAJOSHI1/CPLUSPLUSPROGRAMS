//Swap two numbers without using third variable
#include<iostream>
using namespace std;
int main()
{
    int i=7;
    int j=10;
    cout<<"Vlaue of i is :"<<i<<endl;
    cout<<"Value of j is :"<<j<<endl;
    swap(i,j);
    cout<<"After swapping i is :"<<i<<endl;
    cout<<"After swapping j is :"<<j<<endl;
    return 0;
}